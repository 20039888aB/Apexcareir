const baseSymptomMappings: IRSymptomMapping[] = symptomSeeds.map((symptom, index) => {
  const possibilities = Array.from({ length: 6 }, (_, offset) => diseases[(index * 11 + offset * 7) % diseases.length]);
  return {
    symptom,
    possibilities,
    nextSteps: nextStepTemplates,
    caution:
      'Several conditions can produce similar symptoms; diagnosis needs physical examination, imaging, and laboratory confirmation.',
  };
});

const contextualSymptomMappings: IRSymptomMapping[] = symptomSeeds.flatMap((symptom, symptomIndex) =>
  symptomContexts.map((context, contextIndex) => {
    const start = (symptomIndex * 17 + contextIndex * 9) % diseases.length;
    const possibilities = Array.from({ length: 5 }, (_, offset) => diseases[(start + offset * 5) % diseases.length]);
    return {
      symptom: `${symptom} ${context}`,
      possibilities,
      nextSteps: nextStepTemplates,
      caution:
        'Context can change urgency and differential diagnosis significantly; in-person assessment is recommended for persistent or severe symptoms.',
    };
  }),
);

const symptomMappings = [...baseSymptomMappings, ...contextualSymptomMappings];

